    private void lista_tareasValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lista_tareasValueChanged
        int index = this.lista_tareas.getSelectedIndex();
        if(index > -1){
            Object tarea = this.tareas.get(index);
            DetallesTarea detalles = this.personalDeCoordinacion.consultarDetallesTarea(tarea);
            
            this.textField_Nombre_Tarea.setText(detalles.nombre);
            this.textField_FechaMax.setText(formato.format(detalles.fechaMaxRealizacion));
            if(detalles.fechaFinalizacionReal != null)
                this.textField_FechaFinTarea.setText(formato.format(detalles.fechaFinalizacionReal));
            else
                this.textField_FechaFinTarea.setText(" - - ");
            this.estado_Tarea_Indicador.setText(detalles.estadoTarea);
            this.textField_Equipo.setText(detalles.equipo.toString());
            this.textField_Responsable.setText(detalles.responsable.toString());
        } else{
            this.textField_Nombre_Tarea.setText("");
            this.textField_FechaFinTarea.setText("");
            this.textField_FechaMax.setText("");
            this.estado_Tarea_Indicador.setText(" - - ");
            this.textField_Equipo.setText("");
            this.textField_Responsable.setText("");
        }
    }//GEN-LAST:event_lista_tareasValueChanged

    private void jButton_Modificar_TareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Modificar_TareaActionPerformed
        int index = this.lista_tareas.getSelectedIndex();
        if(index != -1){
            Object tarea = tareas.get(index);
            
            Vista_3_Tarea_Editar vista_Tarea_Editar = new Vista_3_Tarea_Editar( this.personalDeCoordinacion, this, tarea, this.personalDeCoordinacion.consultarDetallesTarea(tarea) );
            this.setVisible(false);
            vista_Tarea_Editar.setVisible(true);
        } else
            JOptionPane.showMessageDialog(this, "Selecciona 1 Tarea para Modificar");
    }//GEN-LAST:event_jButton_Modificar_TareaActionPerformed

    private void jButton_Eliminar_TareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Eliminar_TareaActionPerformed
        int index = this.lista_tareas.getSelectedIndex();
        if(index != -1) {
            Object tarea = this.detallesActividad.tareas.get(index);
            this.personalDeCoordinacion.eliminarTarea(tarea);
            this.actualizarListaTareas();
            JOptionPane.showMessageDialog(this, "Eliminando Tarea '" + tarea.toString() + "'. Tareas En Cruso no serÃ¡n eliminadas");
        } else
            JOptionPane.showMessageDialog(this, "Selecciona 1 Tarea para Eliminar");
    }//GEN-LAST:event_jButton_Eliminar_TareaActionPerformed

