    // Va a la ventana anterior, y volviendo a cargar las actividades
    private void irAnteriorConRecarga() {
        Vista_2_Actividad_Listado vActividades =(Vista_2_Actividad_Listado) this.vAnterior;
        vActividades.refrescar_lista_actividades();
        vAnterior.setVisible(true);
        this.setVisible(false);
    }

    // Muestra el objeto en el combBox (v2.0.0 - v2.0.1)
    private void muestra(JComboBox<String> comboBox, Object obj) {
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        modelo.addElement(obj);
        comboBox.setModel(modelo);
    }
    
    // Se ejecuta cada vez que se crea la tarea con Ã©xito
    public void altaTarea(Object tarea){
        DefaultListModel modelo = (DefaultListModel) lista_tareas.getModel();
        modelo.addElement(tarea);
        lista_tareas.setModel(modelo);
        
        lista_tareas.setSelectedIndex(lista_tareas.getMaxSelectionIndex());
    }
    
