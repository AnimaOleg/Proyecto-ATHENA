    private void finalizar_tarea_jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizar_tarea_jButtonActionPerformed
        //Date fechaFinalizacionReal = Calendar.getInstance().getTime();
        int index = this.lista_tareas.getSelectedIndex();
        if(index != -1){
            Object tarea = this.tareas.get(index);
            
            this.personalDeProyecto.finalizarTarea();
            JOptionPane.showMessageDialog(this, "Si la Tarea estaba En Curso,\nla Tarea '" + tarea.toString() + "' se habrá finalizado");
            limpiar_panel_detalles();
        } else
            JOptionPane.showMessageDialog(this, "Selecciona 1 Tarea");
    }//GEN-LAST:event_finalizar_tarea_jButtonActionPerformed

    private void jButton_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_consultarActionPerformed
        int index = this.lista_tareas.getSelectedIndex();
        if(index != -1){
            Object tarea = this.tareas.get(index);
            DetallesTarea detallesTarea = this.personalDeProyecto.consultarDetallesTarea(tarea);
            
            rellenarDetallesTarea(detallesTarea);
            jPanel_Detalles_Tarea.setVisible(true);
            finalizar_tarea_jButton.setEnabled(true);
        } else
            JOptionPane.showMessageDialog(this, "Selecciona 1 Tarea");
    }//GEN-LAST:event_jButton_consultarActionPerformed

