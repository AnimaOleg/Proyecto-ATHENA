    public void limpiar_panel_detalles() {
        this.nombre.setText("");
        this.fecha_maxima_realizacion.setText("");
        this.fecha_finalizacion_real.setText("");
        this.equipo.setText("");
        this.responsable.setText("");
        this.estado.setText(" - - ");
        finalizar_tarea_jButton.setEnabled(false);
    }
    
    public void rellenarDetallesTarea(DetallesTarea detallesTarea) {
        nombre.setText(detallesTarea.nombre);
        equipo.setText(detallesTarea.equipo.toString());
        responsable.setText(detallesTarea.responsable.toString());
        fecha_maxima_realizacion.setText(formato.format(detallesTarea.fechaMaxRealizacion));
        estado.setText(detallesTarea.estadoTarea);
        if(detallesTarea.fechaFinalizacionReal != null)
            this.fecha_finalizacion_real.setText(formato.format(detallesTarea.fechaFinalizacionReal));
        else
            this.fecha_finalizacion_real.setText(" - - ");
    }
