    private void carga_combo_box(JComboBox<String> comboBox, ArrayList array) {
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        for(Object object: array){
            modelo.addElement(object);
        }
        comboBox.setModel(modelo);
    }
    
    private void irAnteriorConRecarga() {
        Vista_2_Actividad_Detalles vActividades =(Vista_2_Actividad_Detalles) this.vAnterior;
        vActividades.actualizarListaTareas();
        this.vAnterior.setVisible(true);
        this.setVisible(false);
    }
