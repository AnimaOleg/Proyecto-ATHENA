    private void actualizarListaActividades(){
        jList_Actividades.removeAll();
        DefaultListModel modeloLista = new DefaultListModel();
       
        ArrayList actividades = personalDeCoordinacion.consultarActividades();
        for (Object actividad: actividades) {
            String nombre = personalDeCoordinacion.consultarDetallesActividad(actividad).nombre;
            modeloLista.addElement(nombre);
        }
        jList_Actividades.setModel(modeloLista);
    }

