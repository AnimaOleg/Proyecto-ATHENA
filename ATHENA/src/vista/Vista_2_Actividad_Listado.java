    private void jButton_Editar_ActividadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Editar_ActividadActionPerformed
        int index = this.jList_Actividades.getSelectedIndex();
        if(index > -1){
            Object act = actividades.get(index);
            Vista_2_Actividad_Detalles proximaVentana = new Vista_2_Actividad_Detalles(
                    this.personalDeCoordinacion, //< contralador
                    this,                        //< ventana para volver
                    act,                         //< actividad a modificar
                    this.personalDeCoordinacion.consultarDetallesActividad(act) //< detalles de la actividad a modificar
            );
            proximaVentana.setVisible(true);
            this.setVisible(false);
        } else
            JOptionPane.showMessageDialog(this, "Actividad no selecciona");
    }//GEN-LAST:event_jButton_Editar_ActividadActionPerformed

    private void jList_ActividadesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList_ActividadesMouseClicked
        int index = this.jList_Actividades.getSelectedIndex();
        if(index > -1){
            textField_Nombre_Actividad.setText(actividades.get(index).toString());
            Object act = actividades.get(index);
            
            String estadoActividad =  this.personalDeCoordinacion.consultarDetallesActividad(act).estadoActividad;
            label_Estado_Actividad_valor.setText(estadoActividad);
        }         
    }//GEN-LAST:event_jList_ActividadesMouseClicked


